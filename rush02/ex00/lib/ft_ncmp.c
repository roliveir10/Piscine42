int	ft_ncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (i < n && s1[i] && s2[i])
	{
		if (s1[i] != s2[i])
			return ((int)(s1[i] - s2[i]));
		i++;
	}
	if (i < n && (s1[i] || s2[i]))
		return ((int)(s1[i] - s2[i]));
	if (s2[i])
		return (-1);
	return (0);
}
